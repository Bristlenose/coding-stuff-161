public class ProductPLusSum{

   public static void main(String[]args){
      int x = 7;
      int y = 9;
      int z = 3;
      int answer = (x*y*z)+(x+y+z);
      String phrase = ("The product of "+x+", "+ y +" and "+ z+", plus the sum of "+x+", "+ y +" and "+ z+" is: "+answer);
      System.out.println(phrase);
   }
}
